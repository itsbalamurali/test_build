package com.chatak.pay.controller.model.topup;

public class OperatorDTO {

	private Integer operatorID;

	private String operatorSpecs;

	private String operatorName;

	/**
	 * @return the operatorID
	 */
	public Integer getOperatorID() {
		return operatorID;
	}

	/**
	 * @param operatorID
	 *            the operatorID to set
	 */
	public void setOperatorID(Integer operatorID) {
		this.operatorID = operatorID;
	}

	/**
	 * @return the operatorSpecs
	 */
	public String getOperatorSpecs() {
		return operatorSpecs;
	}

	/**
	 * @param operatorSpecs
	 *            the operatorSpecs to set
	 */
	public void setOperatorSpecs(String operatorSpecs) {
		this.operatorSpecs = operatorSpecs;
	}

	/**
	 * @return the operatorName
	 */
	public String getOperatorName() {
		return operatorName;
	}

	/**
	 * @param operatorName
	 *            the operatorName to set
	 */
	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}

}
