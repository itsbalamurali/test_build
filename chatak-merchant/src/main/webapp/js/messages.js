function validInvoiceNumber(id, divId) {
	var val = getVal(id);
	var len = val.length;

	if (isEmpty(val)) {
		setDiv(divId, webMessages.pleaseenterInvoiceNumber);
		loadMsgTitleText();
		return false;
	} else if (!isDigit(val) || len < 4 || len > 16) {
		setDiv(divId, webMessages.shouldbenumeric);
		loadMsgTitleText();
		return false;
	} else {
		setDiv(divId, "");
		return true;
	}
}
