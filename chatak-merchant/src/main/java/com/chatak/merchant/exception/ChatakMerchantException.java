  /**
   * @param arg0
   */
  public ChatakMerchantException(Throwable arg0) {
    super(arg0);
  }

  /**
   * @param arg0
   * @param arg1
   */
  public ChatakMerchantException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

